package it.almaviva.sie.business.model;

import java.io.Serializable;

public class BaseTO implements Serializable {
	private byte[] cronologicoHTML;
    
	private String errorMessage;
	private String utente;
	private String funzione;
	
	

	public String getFunzione() {
		return funzione;
	}

	public void setFunzione(String funzione) {
		this.funzione = funzione;
	}

	public String getUtente() {
		return utente;
	}

	public void setUtente(String utente) {
		this.utente = utente;
	}

	public byte[] getCronologicoHTML() {
		return cronologicoHTML;
	}

	public void setCronologicoHTML(byte[] cronologicoHTML) {
		this.cronologicoHTML = cronologicoHTML;
	}

	public String getErrorMessage() {
		return errorMessage;
	}

	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}
    
    
}
