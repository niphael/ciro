package it.almaviva.sie.business.model;

import java.io.Serializable;

public class ConnessioneMstrTO implements Serializable {
	// Parametri utili alla Connessione MicroStrategy	
	private String sessionID = null;	
	private String saveState = null;


	/**
	 * Costruttore.
	 */
	public ConnessioneMstrTO() {}

	/**
	 * Costruttore.
	 * 
	 * @param sessionID
	 * @param saveState
	 */
	public ConnessioneMstrTO(String sessionID, String saveState) {
		this.sessionID = sessionID;
		this.saveState = saveState;
	}

		
	/**
	 * @return
	 */
	public String getSaveState() {
		return saveState;
	}

	/**
	 * @return
	 */
	public String getSessionID() {
		return sessionID;
	}

	/**
	 * @param string
	 */
	public void setSaveState(String string) {
		saveState = string;
	}

	/**
	 * @param string
	 */
	public void setSessionID(String string) {
		sessionID = string;
	}

}
