package it.almaviva.sie.business.model;

import java.io.Serializable;
import java.util.ArrayList;

public class Utente implements Serializable{

	private String userName;
	private ArrayList listaProfili;
	/*
	 * dati forniti dall'applicativo SIE
	 */
	private Boolean isSimulante=Boolean.FALSE;
	private String utenteSimulato;
	   //
		// Flag per l'opzione di salvataggio delle stampe 
		//
	private int flagMstrSave = 0;
	
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public ArrayList getListaProfili() {
		return listaProfili;
	}
	public void setListaProfili(ArrayList listaProfili) {
		this.listaProfili = listaProfili;
	}
	public Boolean getIsSimulante() {
		return isSimulante;
	}
	public void setIsSimulante(Boolean isSimulante) {
		this.isSimulante = isSimulante;
	}
	public String getUtenteSimulato() {
		return utenteSimulato;
	}
	public void setUtenteSimulato(String utenteSimulato) {
		this.utenteSimulato = utenteSimulato;
	}
	public int getFlagMstrSave() {
		return flagMstrSave;
	}
	public void setFlagMstrSave(int flagMstrSave) {
		this.flagMstrSave = flagMstrSave;
	}
	
}
