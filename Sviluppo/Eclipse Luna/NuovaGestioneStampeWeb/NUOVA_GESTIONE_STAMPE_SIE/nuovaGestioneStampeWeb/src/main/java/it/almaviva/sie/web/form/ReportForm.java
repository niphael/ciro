public class ReportForm extends BaseForm {
	private String formato;
	private String esercizio;
	private String capitoloArticolo;
	public String getFormato() {
		return formato;
	}
	public void setFormato(String formato) {
		this.formato = formato;
	}
	public String getEsercizio() {
		return esercizio;
	}
	public void setEsercizio(String esercizio) {
		this.esercizio = esercizio;
	}
	public String getCapitoloArticolo() {
		return capitoloArticolo;
	}
	public void setCapitoloArticolo(String capitoloArticolo) {
		this.capitoloArticolo = capitoloArticolo;
	}
	
	
}
