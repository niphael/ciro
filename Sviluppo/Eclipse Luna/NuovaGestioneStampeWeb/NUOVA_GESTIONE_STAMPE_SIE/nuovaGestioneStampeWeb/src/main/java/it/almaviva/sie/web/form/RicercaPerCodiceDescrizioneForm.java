package it.almaviva.sie.web.form;

public class RicercaPerCodiceDescrizioneForm extends BaseForm {
	private String codice = null;
	private String descrizione = null;
	private String indexSelezionato = null;
	public String getCodice() {
		return codice;
	}
	public void setCodice(String codice) {
		this.codice = codice;
	}
	public String getDescrizione() {
		return descrizione;
	}
	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}
	public String getIndexSelezionato() {
		return indexSelezionato;
	}
	public void setIndexSelezionato(String indexSelezionato) {
		this.indexSelezionato = indexSelezionato;
	}
	
}
